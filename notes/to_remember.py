# Big O notation -> Time complexity and space complexity


# Recursion -> A function that calls itself which consists of a base case and a recursive case
# DC -> Divide and Conquer -> A problem solving technique that consists of dividing a problem into smaller sub-problems and solving each sub-problem independently (used with recursion)


# Stack -> LIFO (Last In First Out)
# Queue -> FIFO (First In First Out)


# Data structures:
# Array -> A collection of elements identified by index or key - better for reading than linked list, but worse for insertion and deletion
# Linked list -> A collection of elements that are linked to each other - better for insertion and deletion than array, but worse for reading


# Search algorithms:
# Binary search -> A search algorithm that finds the position of a target value within a sorted array - O(log n)


# Sorting algorithms:
# Selection sort -> A sorting algorithm that repeatedly selects the minimum element from an unsorted list and swaps it with the first element - O(n^2)
# Quick sort -> A sorting algorithm that uses a pivot to partition the array into two parts - O(n log n)
